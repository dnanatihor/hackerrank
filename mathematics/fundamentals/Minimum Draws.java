import java.io.*;
import java.util.*;
import java.text.*;
import java.math.*;
import java.util.regex.*;

public class Solution {

    public static void main(String[] args) {
        Scanner in = new Scanner(System.in);
        int T = in.nextInt();
        
        while (T-- > 0) {
            
            int m = in.nextInt();
            System.out.println(m+1);
        }
    }
}